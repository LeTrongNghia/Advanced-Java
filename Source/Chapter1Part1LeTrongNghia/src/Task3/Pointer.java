public class Pointer {
	private float x;//waypoint
	private float y;//waypoint

	public Pointer(float x, float y) {
		super();
		this.x = x;
		this.y = y;
	}
	
	public Pointer() {}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}
	
	
}
